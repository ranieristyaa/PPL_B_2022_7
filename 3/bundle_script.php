	function getXMLHTTPRequest(){
		if (window.XMLHttpRequest){
			return new XMLHttpRequest();
		}
		else{
			return new ActiveXObject("Microsoft.XMLHTTP");
		}
	}
	function add_irs(){
		var xmlhttp = getXMLHTTPRequest();
		var NIM = encodeURI(document.getElementById('NIM').value);
		var semester = encodeURI(document.getElementById('semester').value);
		var sks = encodeURI(document.getElementById('sks').value);

		if(NIM != '' && semester != '' && sks != ''){
			var url = "irs_add.php";
			var inner = "add_response";
			var params = "NIM="+NIM+"&semester="+semester+"&sks="+sks;

			xmlhttp.open('POST',url, true);
			xmlhttp.setRequestHeader("Content-type","application/x-www-form-urlencoded");
			xmlhttp.onreadystatechange = function(){
				document.getElementById(inner).innerHTML = '<img src="images/ajax_loader.png"/>';
				if((xmlhttp.readyState == 4) && (xmlhttp.status == 200)){
					document.getElementById(inner).innerHTML = xmlhttp.responseText;
				}
				return false;
			}
			xmlhttp.send(params);
		}
		else{
			alert("Pleas fill all the fields");
		}
	}
